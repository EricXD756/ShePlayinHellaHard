def movePlayer(grid):
    posX = 1
    posY = 1
    move = "N"
    win = False

    while move != "Q" and win != True:
        print(posX, posY)
        if posX == 10 and posY == 10:
            print("You win!")
            move = "Q"
        move = input("Make a move!(U,D,L,R) or quit 'Q'").upper()

        if move == "D":
            if grid[posX + 1][posY] != "🟥":
                grid[posX + 1][posY] = "🌚"
                grid[posX][posY] = "⬛"
                posX += 1
            else:
                print("Cannot move here")

        if move == "U":
            if grid[posX - 1][posY] != "🟥":
                grid[posX - 1][posY] = "🌚"
                grid[posX][posY] = "⬛"
                posX += -1
                
            else:
                print("Cannot move here")

        if move == "R":
            if grid[posX][posY + 1] != "🟥":
                grid[posX][posY + 1] = "🌚"
                grid[posX][posY] = "⬛"
                posY += 1
            else:
                print("Cannot move here")

        if move == "L":
            if grid[posX][posY - 1] != "🟥":
                grid[posX][posY - 1] = "🌚"
                grid[posX][posY] = "⬛"
                posY += -1
            else:
                print("Cannot move here")

        print("\n".join("".join(row) for row in grid))

