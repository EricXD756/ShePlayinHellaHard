def movePlayer(grid):
    posX = 1
    posY = 1
    move = "N"
    win = False
    while move != "Q" and win != true:
        move = input("Make a move!(U,D,L,R) or quit 'Q'")

        if move == "R":
            if grid[posX + 1][posY] != "🟥":
                grid[posX + 1][posY] = "🌚"
                grid[posX][posY] = "⬛"
                posX += 1
            else:
                print("Cannot move here")

        if move == "L":
            if grid[posX - 1][posY] != "🟥":
                grid[posX - 1][posY] = "🌚"
                grid[posX][posY] = "⬛"
                posX += -1
            else:
                print("Cannot move here")

        if move == "U":
            if grid[posX][posY - 1] != "🟥":
                grid[posX][posY - 1] = "🌚"
                grid[posX][posY] = "⬛"
                posY += -1
            else:
                print("Cannot move here")

        if move == "D":
            if grid[posX][posY - 1] != "🟥":
                grid[posX][posY - 1] = "🌚"
                grid[posX][posY] = "⬛"
                posY += 1
            else:
                print("Cannot move here")

        if posX == 11 and posY == 11:
            print("You win!")
            move = "Q"

